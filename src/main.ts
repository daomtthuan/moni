import { AppRegistry } from 'react-native';
import { App } from '~containers/App';

// --------------------------------------------------------------------------------
// #region - Types and Interfaces
// --------------------------------------------------------------------------------

/** App arguments. */
export type AppArguments = {
  /** App name. */
  name: string;
};

// --------------------------------------------------------------------------------
// #endregion
// --------------------------------------------------------------------------------

// --------------------------------------------------------------------------------
// #region - App
// --------------------------------------------------------------------------------

export function main(args: AppArguments) {
  AppRegistry.registerComponent(args.name, () => App);
}

